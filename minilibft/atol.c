long    atol(char *str)
{
    long ret;
    int sign;

    ret = 0;
    sign = 1;
    while (*str >= 9 && *str <= 13 || *str == ' ')
        *str++;  
    if(*str == '-' || *str == '+')
        sign = (-1 * (*(str++) == '-'));
    while (*str >= 0 && *str <= 9)
        ret = ret * 10 + (*(str++) - '0');
    return (ret * sign);
}